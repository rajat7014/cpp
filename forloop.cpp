#include <iostream>
#include <cstdio>
using namespace std;

int main() {
    // Complete the code.
    
    string st[9] = {"one","two","three","four","five","six","seven","eight","nine"};
    int a;
    int b;
    cin>>a>>b;
    for(int i=a; i<=b;i++){
      if(i<=9){
          cout<<st[i-1]<<endl;
      }
      else{
          if(i%2==0){
              cout<<"even"<<endl;
          }
          else if(i%2!=0){
              cout<<"odd"<<endl;
          }
      }
    }
    return 0;
}
